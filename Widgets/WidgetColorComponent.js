    static delimetrString(string, count) {
        const arr = [];
        for (let i = 0; i < string.length; i += count) {
            arr.push(string.substring(i, i + count));
        }
        return arr;
    }

    static RGBToHex(r, g, b) {
        return "#" + r.toString(16) + g.toString(16) + b.toString(16);
    }
    static HexToRGB(hex) {
        if(!hex) return [255,0,0];
        const del = WidgetColor.delimetrString(hex.replace('#', ''), 2);
        const r = parseInt("0x" + del[0]);
        const g = parseInt("0x" + del[1]);
        const b = parseInt("0x" + del[2]);
        return [r, g, b];
    }



